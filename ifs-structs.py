#
# Définition d'une variable et assignation
#
a = 1

#
# if ... else ... (si ... sinon ...)
#
if a == 10:
    print("ma variable est égale à 10")
else:
    print("ma variable n'est pas égale à 10")

#
# if ... elif ... (si ... sinon si ...)
#
if a == 10:
    print("ma variable est égale à 10")
elif a == 1:
    print("ma variable n'est pas égale à 10")
